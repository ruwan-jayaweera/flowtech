    private void loadCategories() {
        categories = CategoryXMLReader.readCategories();
        Vector vec = new Vector();
        vec.add("---Select Category---");
        for (Iterator<Category> it = categories.iterator(); it.hasNext();) {
            Category category = it.next();
            vec.add(category.getName());
        }
        cmbCategory.setModel(new DefaultComboBoxModel(vec));
    }

    private void loadUnits() {
        int selectedIndex = cmbCategory.getSelectedIndex();

        if (selectedIndex > 0) {
            Vector vec = new Vector();
            Category selectedCategory = categories.get(selectedIndex - 1);
            List<String> units = selectedCategory.getUnits();
            if (units.size() > 1) {
                vec.add("---Unit---");
            }
            for (int i = 0; i < units.size(); i++) {
                vec.add(units.get(i));
            }
            cmbUnit.setModel(new DefaultComboBoxModel(vec));
        }
    }
