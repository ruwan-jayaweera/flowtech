    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        if (itemcode.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Enter Code !", "Save Medicine", 2);
        } else if (brandName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Enter Brand Name !", "Save Medicine", 2);
        } else if (cmbCategory.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Select Category !", "Save Medicine", 2);
        } else if (supplier.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Enter Supplier !", "Save Medicine", 2);
        } else if (unitPrice.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Enter Unit Price !", "Save Medicine", 2);
        } else if (reoderLevel.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Enter Re-order Level !", "Save Medicine", 2);
        } else if (cmbUnit.getSelectedItem().toString().contains("Unit")) {
            JOptionPane.showMessageDialog(null, "Select units !", "Save Medicine", 2);
        } else {
            Item item = new Item();
            item.setItemCode(itemcode.getText());
            item.setBrandName(brandName.getText());
            item.setCategory(cmbCategory.getSelectedItem().toString());
            item.setSupplier(supplier.getText());
            item.setUnitPrice(Double.parseDouble(unitPrice.getText()));
            item.setReorderLevel(Integer.parseInt(reoderLevel.getText()));
            item.setUnit(cmbUnit.getSelectedItem().toString());

            boolean save = itemService.save(item);

            if (save) {
                itemcode.setText(null);
                brandName.setText(null);
                cmbCategory.setSelectedIndex(0);
                cmbUnit.setSelectedIndex(0);
                supplier.setText(null);
                unitPrice.setText(null);
                reoderLevel.setText(null);
            }
        }
    }//GEN-LAST:event_btnsaveActionPerformed

